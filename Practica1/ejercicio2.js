// Definimos una función arrow que calcula el cuadrado de un número
const cuadrado = (numero) => numero * numero;

// Mostramos en consola el resultado de elevar 3 al cuadrado (9)
console.log(cuadrado(3)); // 9

// Mostramos en consola el resultado de elevar 5 al cuadrado (25)
console.log(cuadrado(5)); // 25

// Mostramos en consola el resultado de elevar 10 al cuadrado (100)
console.log(cuadrado(10)); // 100
