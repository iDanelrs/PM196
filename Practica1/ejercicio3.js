// Definimos una función flecha que recibe un nombre y una edad, y devuelve un saludo personalizado
const saludoPersonalizado = (nombre, edad) => `Hola, me llamo ${nombre} y tengo ${edad} años.`;

// Mostramos en consola el saludo personalizado usando los valores "Danel" y 21
console.log(saludoPersonalizado("Danel", 21));